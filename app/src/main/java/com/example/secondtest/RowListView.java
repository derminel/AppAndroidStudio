package net.simplifiedlearning.customlistviewandroid;

public class RowListView {

    int image;
    String name;

    public RowListView(int image, String name) {
        this.image = image;
        this.name = name;
    }

    public int getImage() {
        return image;
    }

    public String getName() {
        return name;
    }
}